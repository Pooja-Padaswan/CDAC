//Write a program to calculate the sum of the first 50 natural numbers.

public class Natural{
			public static void main(String args[]){
						int num = 50;
						int count = 0;
						
						for(int i=1; i<=num; i++)
						{
							count += i;
						}
							System.out.println("The Sum of First 50 Natural Numbers are = " +count);
					}
			}
	
	
